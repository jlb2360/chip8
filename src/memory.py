

class Memory:
    def __init__(self):
        self.ram = bytearray(4096)
        self._load_sprites()

    def _load_sprites(self):
        sprits = [
            0xF0, 0x90, 0x90, 0x90, 0xF0, # the number 0
            0x20, 0x60, 0x20, 0x20, 0x70, # the number 1
             0xF0, 0x10, 0xF0, 0x80, 0xF0,  # the number 2
            0xF0, 0x10, 0xF0, 0x10, 0xF0,  # the number 3
            0x90, 0x90, 0xF0, 0x10, 0x10,  # the number 4
            0xF0, 0x80, 0xF0, 0x10, 0xF0,  # the number 5
            0xF0, 0x80, 0xF0, 0x90, 0xF0,  # the number 6
            0xF0, 0x10, 0x20, 0x40, 0x40,  # the number 7
            0xF0, 0x90, 0xF0, 0x90, 0xF0,  # the number 8
            0xF0, 0x90, 0xF0, 0x10, 0xF0,  # the number 9
            0xF0, 0x90, 0xF0, 0x90, 0x90,  # the number A
            0xE0, 0x90, 0xE0, 0x90, 0xE0,  # the number B
            0xF0, 0x80, 0x80, 0x80, 0xF0,  # the number C
            0xE0, 0x90, 0x90, 0x90, 0xE0,  # the number D
            0xF0, 0x80, 0xF0, 0x80, 0xF0,  # the number E
            0xF0, 0x80, 0xF0, 0x80, 0x80   # the number F
        ]

        self.ram[0:len(sprits)] = sprits

    def load_rom(self, path):
        with open(path, 'rb') as f:
            rom_data = f.read()
        self.ram[0x200:0x200 + len(rom_data)] = rom_data